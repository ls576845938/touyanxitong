def _company(name: str, ticker: str | None = None, market: str | None = None, role: str | None = None) -> dict[str, Any]:
    row: dict[str, Any] = {"name": name}
    if ticker:
        row["ticker"] = ticker
    if market:
        row["market"] = market
    if role:
        row["role"] = role
    return row


def _indicator(name: str, indicator_type: str, code: str | None = None, weight: float | None = None) -> dict[str, Any]:
    row: dict[str, Any] = {"name": name, "type": indicator_type}
    if code:
        row["code"] = code
    if weight is not None:
        row["weight"] = weight
    return row


def _node(
    node_key: str,
    name: str,
    layer: str,
    node_type: str,
    description: str,
    industry_names: list[str],
    tags: list[str],
    anchor_companies: list[dict[str, Any]],
    indicators: list[dict[str, Any]],
) -> dict[str, Any]:
    return {
        "node_key": node_key,
        "name": name,
        "layer": layer,
        "node_type": node_type,
        "description": description,
        "industry_names": industry_names,
        "tags": tags,
        "anchor_companies": anchor_companies,
        "indicators": indicators,
    }


def _edge(source: str, target: str, relation_type: str, flow: str, weight: float) -> dict[str, Any]:
    return {
        "source": source,
        "target": target,
        "relation_type": relation_type,
        "flow": flow,
        "weight": weight,
    }


def _region(
    region_key: str,
    label: str,
    x: float,
    y: float,
    geo_role: str,
    specialty: str,
    node_keys: list[str],
    listed_hubs: list[str],
) -> dict[str, Any]:
    return {
        "region_key": region_key,
        "label": label,
        "x": x,
        "y": y,
        "geo_role": geo_role,
        "specialty": specialty,
        "node_keys": node_keys,
        "listed_hubs": listed_hubs,
    }


