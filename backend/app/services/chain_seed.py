DEFAULT_FOCUS_NODE_KEY = "ai_compute"


def _validate_seed() -> None:
    if len(CHAIN_LAYERS) != 8:
        raise ValueError("CHAIN_LAYERS must contain 8 layers")

    node_keys = [node["node_key"] for node in CHAIN_NODES]
    if len(node_keys) != len(set(node_keys)):
        raise ValueError("CHAIN_NODES contains duplicate node_key values")
    if not 60 <= len(CHAIN_NODES) <= 120:
        raise ValueError("CHAIN_NODES count is out of expected range")

    valid_layers = set(CHAIN_LAYERS)
    for node in CHAIN_NODES:
        if node["layer"] not in valid_layers:
            raise ValueError(f"unknown layer for node {node['node_key']}")
        for field in ("industry_names", "tags", "anchor_companies", "indicators"):
            if not node[field]:
                raise ValueError(f"node {node['node_key']} missing {field}")

    key_set = set(node_keys)
    if not 100 <= len(CHAIN_EDGES) <= 220:
        raise ValueError("CHAIN_EDGES count is out of expected range")
    for edge in CHAIN_EDGES:
        if edge["source"] not in key_set or edge["target"] not in key_set:
            raise ValueError(f"edge references unknown node: {edge}")

    if not WORLD_REGIONS:
        raise ValueError("WORLD_REGIONS cannot be empty")
    for region in WORLD_REGIONS:
        if not region["node_keys"]:
            raise ValueError(f"region {region['region_key']} missing node mappings")
        missing = [node_key for node_key in region["node_keys"] if node_key not in key_set]
        if missing:
            raise ValueError(f"region {region['region_key']} references unknown nodes: {missing}")

    if DEFAULT_FOCUS_NODE_KEY not in key_set:
        raise ValueError("DEFAULT_FOCUS_NODE_KEY must exist in CHAIN_NODES")


_validate_seed()


